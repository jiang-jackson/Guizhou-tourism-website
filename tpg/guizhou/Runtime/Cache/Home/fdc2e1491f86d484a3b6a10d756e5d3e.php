							<p>黔东南苗族称剪纸为“剪花”、“苗花纸”。在长期的刺绣实践中，艺人们发现用剪纸图案来代替在绣面上描绘图案，可以准确无误地不断复制，既省工又省时，同时也能保持绣面的干净洁亮，从此，她们就把剪纸图案作为刺绣艺术的“蓝本”。为了保证刺绣的质量，苗族妇女们把她们的聪明才华和喜怒哀乐等审美观融人到剪纸图案中，使之与众不同，具有鲜明的民族特色。<br><br>
